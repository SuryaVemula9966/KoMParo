const volumeRange = document.getElementById('volumeRange');
const volumeIndicator = document.getElementById('volumeIndicator');
const volumeLabel = document.getElementById('volumeLabel');
let volumeLevel = 50; // Initial volume level (0-100)

// Function to update volume level
const updateVolume = () => {
    volumeIndicator.style.left = volumeLevel + '%';
    // Update volume label based on volume level
    if (volumeLevel < 30) {
        volumeLabel.innerText = 'Low';
    } else if (volumeLevel > 70) {
        volumeLabel.innerText = 'High';
    } else {
        volumeLabel.innerText = 'Medium';
    }
};

// Update volume on click
volumeRange.addEventListener('click', (event) => {
    const rect = volumeRange.getBoundingClientRect();
    const clickX = event.clientX - rect.left;
    volumeLevel = Math.min(Math.max((clickX / rect.width) * 100, 0), 100);
    updateVolume();
});